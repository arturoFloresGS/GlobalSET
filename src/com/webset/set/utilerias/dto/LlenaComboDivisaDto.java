package com.webset.set.utilerias.dto;

public class LlenaComboDivisaDto {

	private String idDivisa;
	private String descDivisa;
	private String idDivisaSoin;
	private String clasificacion;
	
	public String getIdDivisa() {
		return idDivisa;
	}
	public void setIdDivisa(String idDivisa) {
		this.idDivisa = idDivisa;
	}
	public String getDescDivisa() {
		return descDivisa;
	}
	public void setDescDivisa(String descDivisa) {
		this.descDivisa = descDivisa;
	}
	public String getIdDivisaSoin() {
		return idDivisaSoin;
	}
	public void setIdDivisaSoin(String idDivisaSoin) {
		this.idDivisaSoin = idDivisaSoin;
	}
	public String getClasificacion() {
		return clasificacion;
	}
	public void setClasificacion(String clasificacion) {
		this.clasificacion = clasificacion;
	}
}
