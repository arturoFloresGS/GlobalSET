package com.webset.set.utilerias.dto;

public class RetornoLayoutDto {
	private int importados;
	private String mensajes;
	private String mensajesUsuario;
	private String psArchivos;
	public int getImportados() {
		return importados;
	}
	public void setImportados(int importados) {
		this.importados = importados;
	}
	public String getMensajes() {
		return mensajes;
	}
	public void setMensajes(String mensajes) {
		this.mensajes = mensajes;
	}
	public String getMensajesUsuario() {
		return mensajesUsuario;
	}
	public void setMensajesUsuario(String mensajesUsuario) {
		this.mensajesUsuario = mensajesUsuario;
	}
	public String getPsArchivos() {
		return psArchivos;
	}
	public void setPsArchivos(String psArchivos) {
		this.psArchivos = psArchivos;
	}
}
