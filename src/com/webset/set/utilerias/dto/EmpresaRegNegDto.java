package com.webset.set.utilerias.dto;

public class EmpresaRegNegDto {

	private int noEmpresa;
	private String nombreEmpresa;
	private String horaLimiteOperacion;
	
	public int getNoEmpresa() {
		return noEmpresa;
	}
	public void setNoEmpresa(int noEmpresa) {
		this.noEmpresa = noEmpresa;
	}
	public String getNombreEmpresa() {
		return nombreEmpresa;
	}
	public void setNombreEmpresa(String nombreEmpresa) {
		this.nombreEmpresa = nombreEmpresa;
	}
	public String getHoraLimiteOperacion() {
		return horaLimiteOperacion;
	}
	public void setHoraLimiteOperacion(String horaLimiteOperacion) {
		this.horaLimiteOperacion = horaLimiteOperacion;
	}
	
}
