package com.webset.set.utilerias.dto;

public class DoctoTesoreriaDto {

	private int idDoctoTes;
	private String incluir;
	private String excluir;
	private String clasificacion;
	
	public int getIdDoctoTes() {
		return idDoctoTes;
	}
	public void setIdDoctoTes(int idDoctoTes) {
		this.idDoctoTes = idDoctoTes;
	}
	public String getIncluir() {
		return incluir;
	}
	public void setIncluir(String incluir) {
		this.incluir = incluir;
	}
	public String getExcluir() {
		return excluir;
	}
	public void setExcluir(String excluir) {
		this.excluir = excluir;
	}
	public String getClasificacion() {
		return clasificacion;
	}
	public void setClasificacion(String clasificacion) {
		this.clasificacion = clasificacion;
	}
	
}
