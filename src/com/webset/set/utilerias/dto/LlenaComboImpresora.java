package com.webset.set.utilerias.dto;

public class LlenaComboImpresora {
	private int noImpresora;
	private int cjaCve;
	private int numCharola;
	private int noBanco;
	private String descBanco;
	private String descripcion;
	private String tipoImpresora;
	public String getTipoImpresora() {
		return tipoImpresora;
	}
	public void setTipoImpresora(String tipoImpresora) {
		this.tipoImpresora = tipoImpresora;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getNoImpresora() {
		return noImpresora;
	}
	public void setNoImpresora(int noImpresora) {
		this.noImpresora = noImpresora;
	}
	public int getCjaCve() {
		return cjaCve;
	}
	public void setCjaCve(int cjaCve) {
		this.cjaCve = cjaCve;
	}
	public int getNumCharola() {
		return numCharola;
	}
	public void setNumCharola(int numCharola) {
		this.numCharola = numCharola;
	}
	public int getNoBanco() {
		return noBanco;
	}
	public void setNoBanco(int noBanco) {
		this.noBanco = noBanco;
	}
	public String getDescBanco() {
		return descBanco;
	}
	public void setDescBanco(String descBanco) {
		this.descBanco = descBanco;
	}

}
