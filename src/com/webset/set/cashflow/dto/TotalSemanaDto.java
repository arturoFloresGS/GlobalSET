package com.webset.set.cashflow.dto;

public class TotalSemanaDto {
	
	private int usuario;
	private String ingreso_egreso;
	private int id_tipo_concepto;
	private String descripcion;
	private double total;
	private int row_tipo_concepto;
	private String identificador;
	private String desc_larga;
	private String mes;
	private int columna;
	private String divisa;
	public int getUsuario() {
		return usuario;
	}
	public void setUsuario(int usuario) {
		this.usuario = usuario;
	}
	public String getIngreso_egreso() {
		return ingreso_egreso;
	}
	public void setIngreso_egreso(String ingreso_egreso) {
		this.ingreso_egreso = ingreso_egreso;
	}
	public int getId_tipo_concepto() {
		return id_tipo_concepto;
	}
	public void setId_tipo_concepto(int id_tipo_concepto) {
		this.id_tipo_concepto = id_tipo_concepto;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	public int getRow_tipo_concepto() {
		return row_tipo_concepto;
	}
	public void setRow_tipo_concepto(int row_tipo_concepto) {
		this.row_tipo_concepto = row_tipo_concepto;
	}
	public String getIdentificador() {
		return identificador;
	}
	public void setIdentificador(String identificador) {
		this.identificador = identificador;
	}
	public String getDesc_larga() {
		return desc_larga;
	}
	public void setDesc_larga(String desc_larga) {
		this.desc_larga = desc_larga;
	}
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	public int getColumna() {
		return columna;
	}
	public void setColumna(int columna) {
		this.columna = columna;
	}
	public String getDivisa() {
		return divisa;
	}
	public void setDivisa(String divisa) {
		this.divisa = divisa;
	}
	
	

}
