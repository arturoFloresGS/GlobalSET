package com.webset.set.inversiones.dto;

public class ContratoInstitucionDto {
	
	private int noEmpresa;
	private String nomEmpresa;
	private String contrato;
	/**
	 * @return the noEmpresa
	 */
	public int getNoEmpresa() {
		return noEmpresa;
	}
	/**
	 * @param noEmpresa the noEmpresa to set
	 */
	public void setNoEmpresa(int noEmpresa) {
		this.noEmpresa = noEmpresa;
	}
	/**
	 * @return the nomEmpresa
	 */
	public String getNomEmpresa() {
		return nomEmpresa;
	}
	/**
	 * @param nomEmpresa the nomEmpresa to set
	 */
	public void setNomEmpresa(String nomEmpresa) {
		this.nomEmpresa = nomEmpresa;
	}
	/**
	 * @return the contrato
	 */
	public String getContrato() {
		return contrato;
	}
	/**
	 * @param contrato the contrato to set
	 */
	public void setContrato(String contrato) {
		this.contrato = contrato;
	}
	

}
