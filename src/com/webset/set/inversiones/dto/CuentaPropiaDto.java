package com.webset.set.inversiones.dto;

public class CuentaPropiaDto {
	private int idBanco;
	private String idChequera;
	private String idChequeraDos;
	
	public int getIdBanco() {
		return idBanco;
	}
	public void setIdBanco(int idBanco) {
		this.idBanco = idBanco;
	}
	public String getIdChequera() {
		return idChequera;
	}
	public void setIdChequera(String idChequera) {
		this.idChequera = idChequera;
	}
	public String getIdChequeraDos() {
		return idChequeraDos;
	}
	public void setIdChequeraDos(String idChequeraDos) {
		this.idChequeraDos = idChequeraDos;
	}

}
