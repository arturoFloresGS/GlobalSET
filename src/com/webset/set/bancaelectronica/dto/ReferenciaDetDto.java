package com.webset.set.bancaelectronica.dto;

public class ReferenciaDetDto {
    int iBanco;
    int iEmpresa;
    int iCaso;
    int iSecuencia;
    String  sVerifCampo;
    String  sCondicion;
    String  sValor;
    String  sCorresp;
    String  sBImporta;
    String  sIdentificador;
    String  sTipoId;
    
	public int getIBanco() {
		return iBanco;
	}
	public void setIBanco(int banco) {
		iBanco = banco;
	}
	public int getIEmpresa() {
		return iEmpresa;
	}
	public void setIEmpresa(int empresa) {
		iEmpresa = empresa;
	}
	public int getICaso() {
		return iCaso;
	}
	public void setICaso(int caso) {
		iCaso = caso;
	}
	public int getISecuencia() {
		return iSecuencia;
	}
	public void setISecuencia(int secuencia) {
		iSecuencia = secuencia;
	}
	public String getSVerifCampo() {
		return sVerifCampo;
	}
	public void setSVerifCampo(String verifCampo) {
		sVerifCampo = verifCampo;
	}
	public String getSCondicion() {
		return sCondicion;
	}
	public void setSCondicion(String condicion) {
		sCondicion = condicion;
	}
	public String getSValor() {
		return sValor;
	}
	public void setSValor(String valor) {
		sValor = valor;
	}
	public String getSCorresp() {
		return sCorresp;
	}
	public void setSCorresp(String corresp) {
		sCorresp = corresp;
	}
	public String getSBImporta() {
		return sBImporta;
	}
	public void setSBImporta(String importa) {
		sBImporta = importa;
	}
	public String getSIdentificador() {
		return sIdentificador;
	}
	public void setSIdentificador(String identificador) {
		sIdentificador = identificador;
	}
	public String getSTipoId() {
		return sTipoId;
	}
	public void setSTipoId(String tipoId) {
		sTipoId = tipoId;
	}

}
