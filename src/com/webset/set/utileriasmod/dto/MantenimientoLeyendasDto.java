package com.webset.set.utileriasmod.dto;

public class MantenimientoLeyendasDto {
	private int idLeyenda;
	private String descLeyenda;
	private String usuarioAlta;
	private String fecAlta;
		
	public int getIdLeyenda() {
		return idLeyenda;
	}
	public void setIdLeyenda(int idLeyenda) {
		this.idLeyenda = idLeyenda;
	}
	public String getDescLeyenda() {
		return descLeyenda;
	}
	public void setDescLeyenda(String descLeyenda) {
		this.descLeyenda = descLeyenda;
	}
	public String getUsuarioAlta() {
		return usuarioAlta;
	}
	public void setUsuarioAlta(String usuarioAlta) {
		this.usuarioAlta = usuarioAlta;
	}
	public String getFecAlta() {
		return fecAlta;
	}
	public void setFecAlta(String fecAlta) {
		this.fecAlta = fecAlta;
	}	
	
}
