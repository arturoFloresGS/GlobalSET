package com.webset.set.utileriasmod.dto;

public class EquivalenciaEmpresasDto {
	
	int noEmpresa;
	String nomEmpresa;
	String descripcion;
	String empresaSet;
	String empresaInterface;
	String codigo;
	
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getEmpresaSet() {
		return empresaSet;
	}
	public void setEmpresaSet(String empresaSet) {
		this.empresaSet = empresaSet;
	}
	public String getEmpresaInterface() {
		return empresaInterface;
	}
	public void setEmpresaInterface(String empresaInterface) {
		this.empresaInterface = empresaInterface;
	}
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public int getNoEmpresa() {
		return noEmpresa;
	}
	public void setNoEmpresa(int noEmpresa) {
		this.noEmpresa = noEmpresa;
	}
	public String getNomEmpresa() {
		return nomEmpresa;
	}
	public void setNomEmpresa(String nomEmpresa) {
		this.nomEmpresa = nomEmpresa;
	}

}
